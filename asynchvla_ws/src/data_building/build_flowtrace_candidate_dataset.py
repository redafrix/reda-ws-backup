#!/usr/bin/env python3
"""Stage 7 — Build candidate dataset from a flow-trace multiseed trace file.

Mirrors `asynchvla_ws/src/data_building/build_multiseed_candidate_dataset.py`
but additionally carries the `candidate_flow_trace_summary` dict per row when
the candidate is one of the SimVLA seeds. Expert/perturb/cross-task candidates
do not have flow traces (they were never generated by SimVLA in this context).
"""

from __future__ import annotations

import argparse
import json
import os
from collections import defaultdict
from pathlib import Path

import torch

REDA_WS = Path(os.environ.get("REDA_WS", "/media/rootalkhatib/My Passport/reda_ws"))
SRC_ROOT = REDA_WS / "asynchvla_ws/data/processed_stage7_flow"
DST_ROOT = REDA_WS / "asynchvla_ws/data/processed_stage7_flow"


def add(rows, ctx, typ, act, flow_summary=None):
    exp = ctx["expert_normalized_action"]
    per = torch.linalg.vector_norm(act - exp, dim=-1)
    rows.append({
        "context_id": ctx["context_id"],
        "sample_id": ctx["sample_id"],
        "task_name": ctx["task_name"],
        "source_hdf5": ctx["source_hdf5"],
        "language_instruction": ctx["language_instruction"],
        "candidate_type": typ,
        "candidate_action_normalized": act.clone(),
        "target_expert_action_normalized": exp.clone(),
        "true_per_step_l2_error": per.clone(),
        "true_chunk_l2_error": float(per.mean()),
        "pooled_vlm_features": ctx["pooled_vlm_features"].clone(),
        "proprio": ctx["proprio"].clone(),
        "split": ctx["split"],
        "candidate_flow_trace_summary": flow_summary or {},
    })


def build(trace_path: Path, out_path: Path, seed: int = 123):
    if not trace_path.exists():
        return None
    data = torch.load(trace_path, map_location="cpu", weights_only=False)
    samples = data["samples"]
    seeds = data.get("seeds", [0, 1, 2, 3, 4])
    g = torch.Generator().manual_seed(seed)

    bytask = defaultdict(list)
    for i, s in enumerate(samples):
        bytask[s["task_name"]].append(i)

    rows = []
    for i, s in enumerate(samples):
        exp = s["expert_normalized_action"]
        add(rows, s, "expert_action", exp, flow_summary={})
        for seed_idx in seeds:
            seed_act = s[f"simvla_seed{seed_idx}_normalized_action"]
            flow = s.get(f"simvla_seed{seed_idx}_flow_trace_summary", {})
            add(rows, s, f"simvla_seed{seed_idx}", seed_act, flow_summary=flow)
        add(rows, s, "perturb_small", exp + 0.10 * torch.randn(exp.shape, generator=g),
            flow_summary={})
        add(rows, s, "perturb_large", exp + 0.75 * torch.randn(exp.shape, generator=g),
            flow_summary={})
        same = [j for j in bytask[s["task_name"]] if j != i]
        if same:
            far = max(same, key=lambda j: abs(j - i))
            add(rows, s, "same_task_far", samples[far]["expert_normalized_action"], {})
        other = [j for j, x in enumerate(samples) if x["task_name"] != s["task_name"]]
        if other:
            add(rows, s, "other_demo_or_other_task",
                samples[other[(i * 37) % len(other)]]["expert_normalized_action"], {})

    torch.save({
        "schema_version": "flowtrace_multiseed_candidate_v1",
        "source_trace": str(trace_path),
        "num_contexts": len(samples),
        "num_candidates": len(rows),
        "candidates": rows,
    }, out_path)
    return len(samples), len(rows)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--split-name", required=True)
    args = ap.parse_args()
    d = SRC_ROOT / args.split_name
    if not d.exists():
        raise SystemExit(f"missing split dir {d}")
    summary = {}
    for part in ("train", "calib", "test_id", "test_ood"):
        res = build(d / f"flowtrace_multiseed_trace_{part}.pt",
                    d / f"flowtrace_multiseed_candidate_{part}.pt")
        if res:
            summary[part] = {"contexts": res[0], "candidates": res[1]}
            print(part, res)
    (d / "flowtrace_multiseed_candidate_build_summary.json").write_text(
        json.dumps(summary, indent=2))
    print("done", args.split_name)


if __name__ == "__main__":
    main()
